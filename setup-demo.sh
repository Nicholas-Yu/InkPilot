# 雪中悍刀行

## 项目简介

北凉世子徐凤年，在外人看来是个纨绔子弟，实则胸有沟壑。为逃避做隋珠公主的驸马，在老仆老黄的陪伴下，带着二姐徐渭熊赠予的丫鬟姜泥游历江湖三年。归来的徐凤年开始踏上一条属于自己的道路——练刀、习武、统军、守北凉。

## 故事梗概

庙堂与江湖交织，权谋与侠义并行。徐凤年从一个被人看不起的纨绔世子，一步步成长为真正的北凉王。他要面对的不仅是北莽百万铁骑，还有离阳王朝的猜忌与算计。

## 世界观设定

- **离阳王朝**: 当朝政权，对北凉既依赖又忌惮
- **北凉**: 镇守西北边疆，抵御北莽入侵
- **北莽**: 北方游牧政权，虎视眈眈
- **江湖**: 各大宗门势力，武道境界分明
- **武道境界**: 金刚、指玄、天象、陆地神仙、天人

## 主要角色

徐凤年、姜泥、白狐儿脸（南宫仆射）、老黄、徐骁、陈芝豹、李淳罡等

## 写作计划

以徐凤年成长为主线，穿插庙堂权谋与江湖恩怨

## 标签

武侠、权谋、热血、群像
EOF

