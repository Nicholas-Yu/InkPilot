function getStyleInstruction(settings) {
  const style = settings.writingStyle || 'serious';
  if (style === 'custom' && settings.customStyleDesc) {
    return `文风要求：${settings.customStyleDesc}`;
  }
  return STYLES[style]?.instruction || STYLES.serious.instruction;
}

